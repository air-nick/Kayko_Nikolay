# Исходный список чисел
my_list = [42, 69, 322, 13, 0, 99, -5, 9, 8, 7, -6, 5]

# Инициализируем индекс и пустой список для положительных чисел
i = 0
positive_numbers = []

# Запускаем цикл while
while i < len(my_list):
    # Если встречаем отрицательное число, выходим из цикла
    if my_list[i] < 0:
        break
    # Если число положительное, добавляем его в список
    if my_list[i] > 0:
        positive_numbers.append(my_list[i])
    i += 1

# Выводим список положительных чисел до первого отрицательного
print(positive_numbers)
