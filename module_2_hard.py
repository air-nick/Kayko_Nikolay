import random # Импортируем библиотеку

def generate_password(n):
    result = ""  # Инициализация пустой строки для результата
    # Перебираем все числа от 1 до 20 для первого числа пары
    for i in range(1, 21):
        # Перебираем числа для второго числа пары, начиная с i, чтобы пары не повторялись
        for j in range(i, 21):
            pair_sum = i + j  # Вычисляем сумму текущей пары чисел
            # Проверяем, делится ли n на сумму этой пары без остатка
            if n % pair_sum == 0:
                result += str(i) + str(j)  # Если делится, добавляем пару в строку результата
    return result  # Возвращаем итоговую строку

# Генерируем случайное число от 3 до 20
n = random.randint(3, 20)
print(f"Случайное число: {n}")
password = generate_password(n)
print(f"Нужный пароль: {password}")
